// 定义使用的GPIO引脚
#define PIN_MISO 4
#define PIN_CS   5
#define PIN_SCK  6
#define PIN_MOSI 7
#define PIN_DC   8
#define PIN_RST  9

void init_spi() {
    // 初始化GPIO为SPI功能
    spi_init(spi0, 1000 * 1000); // 初始化SPI0, 速率1MHz
    gpio_set_function(PIN_MISO, GPIO_FUNC_SPI);
    gpio_set_function(PIN_CS,   GPIO_FUNC_SIO);
    gpio_set_function(PIN_SCK,  GPIO_FUNC_SPI);
    gpio_set_function(PIN_MOSI, GPIO_FUNC_SPI);
    gpio_init(PIN_DC);
    gpio_init(PIN_RST);
    gpio_set_dir(PIN_DC, GPIO_OUT);
    gpio_set_dir(PIN_RST, GPIO_OUT);

    gpio_init(25);
    gpio_set_dir(25, GPIO_OUT);
}

void ssd1306_write_byte(uint8_t chData, uint8_t chCmd) {
    gpio_put(PIN_CS, 0); // CS置低

    if (chCmd) {
        gpio_put(PIN_DC, 1); // DC置高
    } else {
        gpio_put(PIN_DC, 0); // DC置低
    }

    spi_write_blocking(spi0, &chData, 1); // 发送数据

    gpio_put(PIN_CS, 1); // CS置高
}

